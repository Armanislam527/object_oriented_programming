# Variable Declaration
name = "Alice"
age = 30

# Control Structures
if age >= 18:
    print("Adult")
else:
    print("Minor")

# Function Definition
def greet(name):
    return f"Hello, {name}!"

# Class Definition
class Animal:
    def __init__(self, name):
        self.name = name

    def speak(self):
        return "I am an animal."

# Object Creation
dog = Animal("Dog")
print(dog.speak())  # Output: I am an animal.